package com.Java;

public class Identifier
// name of java code/method_name/class_name/Variable_name is called as identier.
{
	public static void main(String[] args)
	//main is method inside code.
	{
		int x=10;
		//x is variable inside code
		System.out.println(+x);
	}
}

//identifiers inside code =1.identifier  2.main  3.string  4.args  5. x

//Rules of identifier:
	/*
	 	character and special characters are allowed A-Z,a-z,0-9,_,$
		Identifier can't start with digit.
		case sensitive
		no limit for java identifier (>15 recommended)
		reserved word can't used.
	 */
